def tinh_khoang_cach(a, b):
    R = 6371
    lat1, lon1 = a
    lat2, lon2 = b
    dlat = radians(lat2-lat1)
    dlon = radians(lon2-lon1)
    x = sin(dlat/2)**2 + cos(radians(lat1))*cos(radians(lat2))*sin(dlon/2)**2
    return 2*R*atan2(sqrt(x), sqrt(1-x)) * 1.3

def tinh_gia(don, den, gt, nc, tt, dg, dg2):
    sim = ctrl.ControlSystemSimulation(he_thong)

    kc = tinh_khoang_cach(dia_diem[don], dia_diem[den])

    sim.input['khoang_cach'] = min(kc, 50)
    sim.input['giao_thong'] = gt
    sim.input['nhu_cau'] = nc
    sim.input['thoi_tiet'] = tt
    sim.input['danh_gia'] = dg
    sim.input['dung_gio'] = dg2

    sim.compute()

    he_so = sim.output.get('he_so_gia', 1.5)
    thuong = sim.output.get('diem_thuong', 50)

    gia = (12000 + kc*4500) * he_so

    return kc, gia, he_so, thuong

