def phan_loai_gia(he_so):
    if he_so < 1.3:
        return "Thấp"
    elif he_so < 1.8:
        return "Trung bình"
    elif he_so < 2.4:
        return "Cao"
    else:
        return "Rất cao"

def phan_loai_thuong(diem):
    if diem < 20:
        return "Không có"
    elif diem < 50:
        return "Ít"
    elif diem < 80:
        return "Trung bình"
    else:
        return "Cao"

def ve_ban_do(diem_don, diem_den, ten_don, ten_den, gia, kc):
    lat_giua = (diem_don[0] + diem_den[0]) / 2
    lon_giua = (diem_don[1] + diem_den[1]) / 2

    ban_do = folium.Map(location=[lat_giua, lon_giua], zoom_start=13)

    folium.Marker(
        location=diem_don,
        popup=f"Điểm đón: {ten_don}",
        tooltip="Điểm đón",
        icon=folium.Icon(color="green")
    ).add_to(ban_do)

    folium.Marker(
        location=diem_den,
        popup=f"Điểm đến: {ten_den}",
        tooltip="Điểm đến",
        icon=folium.Icon(color="red")
    ).add_to(ban_do)

    folium.PolyLine(
        locations=[diem_don, diem_den],
        weight=5,
        opacity=0.8
    ).add_to(ban_do)

    folium.Marker(
        location=[lat_giua, lon_giua],
        popup=f"Khoảng cách ước tính: {kc:.2f} km<br>Giá: {gia:,.0f} VNĐ",
        tooltip="Thông tin chuyến đi",
        icon=folium.Icon(color="blue")
    ).add_to(ban_do)

    return ban_do

